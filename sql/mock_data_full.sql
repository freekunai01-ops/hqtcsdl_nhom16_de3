-- ==== CNTT K20: MALTC 1-48 du diem ====
DECLARE @sv INT, @ltc INT, @m VARCHAR(10), @cc INT, @gk FLOAT, @ck FLOAT;
SET @sv = 1;
WHILE @sv <= 25
BEGIN
    SET @m = 'N20CN' + RIGHT('0000'+CAST(@sv AS VARCHAR),4);
    DECLARE c1 CURSOR FOR SELECT MALTC FROM LOPTINCHI WHERE MALTC BETWEEN 1 AND 48 AND HUYLOP=0;
    OPEN c1; FETCH NEXT FROM c1 INTO @ltc;
    WHILE @@FETCH_STATUS = 0
    BEGIN
        SET @cc = 6 + CAST(ROUND(RAND(CHECKSUM(NEWID()))*4,0) AS INT);
        SET @gk = ROUND((4.0+RAND(CHECKSUM(NEWID()))*6.0)*2,0)/2;
        SET @ck = ROUND((4.0+RAND(CHECKSUM(NEWID()))*6.0)*2,0)/2;
        IF @cc>10 SET @cc=10; IF @gk>10 SET @gk=10.0; IF @ck>10 SET @ck=10.0;
        -- Vai SV rot de demo
        IF @sv=8  AND @ltc%11=0 SET @ck=ROUND((1.0+RAND(CHECKSUM(NEWID()))*2.0)*2,0)/2;
        IF @sv=15 AND @ltc%7=0  SET @ck=ROUND((2.0+RAND(CHECKSUM(NEWID()))*1.5)*2,0)/2;
        IF @ck>10 SET @ck=10.0; IF @ck<0 SET @ck=0;
        INSERT INTO DANGKY(MALTC,MASV,DIEM_CC,DIEM_GK,DIEM_CK,HUYDANGKY) VALUES(@ltc,@m,@cc,@gk,@ck,0);
        FETCH NEXT FROM c1 INTO @ltc;
    END;
    CLOSE c1; DEALLOCATE c1;
    SET @sv += 1;
END;
GO

