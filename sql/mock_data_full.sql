-- K20: MALTC 1-48, tat ca co diem
DECLARE @sv INT, @ltc INT, @m VARCHAR(10), @cc INT, @gk FLOAT, @ck FLOAT;
SET @sv = 1;
WHILE @sv <= 25
BEGIN
    SET @m = 'N20CN' + RIGHT('0000' + CAST(@sv AS VARCHAR), 4);
    DECLARE c1 CURSOR FOR
        SELECT MALTC FROM LOPTINCHI WHERE MALTC BETWEEN 1 AND 48 AND HUYLOP=0;
    OPEN c1; FETCH NEXT FROM c1 INTO @ltc;
    WHILE @@FETCH_STATUS = 0
    BEGIN
        SET @cc = 6 + ROUND(RAND(CHECKSUM(NEWID()))*4,0);
        SET @gk = ROUND(4.5+RAND(CHECKSUM(NEWID()))*5.5,1);
        SET @ck = ROUND(4.5+RAND(CHECKSUM(NEWID()))*5.5,1);
        IF @sv%8=0 AND @ltc%11=0 SET @ck = ROUND(1.5+RAND(CHECKSUM(NEWID()))*2.0,1);
        IF @cc>10 SET @cc=10; IF @gk>10 SET @gk=10.0; IF @ck>10 SET @ck=10.0;
        INSERT INTO DANGKY VALUES(@ltc,@m,@cc,@gk,@ck,0);
        FETCH NEXT FROM c1 INTO @ltc;
    END;
    CLOSE c1; DEALLOCATE c1;
    SET @sv += 1;
END;
